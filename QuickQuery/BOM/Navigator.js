function attribute() {
  navigator.appCodeName; // 浏览器名称，通常是Mozilla
  navigator.appMinorVersion; //版本信息
  navigator.appName; // 完整浏览器名称
  navigator.appVersion; // 浏览器版本
  navigator.buildId; // 浏览器编译版本
  navigator.cookieEnabled; // 表示cooike是否启用
  navigator.cpuClass; // 计算机中使用CPU类型
  navigator.javaEnabled; // 表示当前浏览器是否启用了java
  navigator.language; // 浏览器的主语言
  navigator.mimeTypes; // 在浏览器中注册的MIME类型数组
  navigator.onLine; // 表示当前浏览器是否连接了因特网
  navigator.oscpu; // 客户端使用的操作系统或CPU
  navigator.platform; // 浏览器所在的系统平台
  navigator.plugins; // 浏览器中安装的插件数组 
  navigator.preference(); // 设置用户的首选项
  navigator.product; // 产品名称
  navigator.productSub; // 关于产品的次要信息
  navigator.register - ContentHandler(); // 针对特定的MIME类型将一个站点注册为处理程序
  navigator.register - ProtocolHandler(); // 针对特定协议将一个站点注册为处理程序
  navigator.userAgent; // 浏览器用户代理的字符串
  navigator.userLanguage; // 操作系统的默认语言
  navigator.userProfile; // 访问用户的个人信息
  navigator.vendor; // 浏览器的品牌
  navigator.vendorSub; // 有关供应商的次要信息
}
